// # Loop with Range

// Buatlah script untuk mencetak dengan rentang angka tertentu.

// ### Contoh

// ```
// Input pertama: 4
// Input kedua: 8
// Output di browser: 4, 5, 6, 7, 8
// ```

let angkaPertama = 4;
let angkaKedua = 8;
for(let i = angkaPertama ; i <= angkaKedua ; i++){
    console.log(i);
}